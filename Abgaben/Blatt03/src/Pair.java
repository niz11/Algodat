
public class Pair<T> {
	private T first = null;
	private T second = null;
	private T swapHolder = null;
	Pair(T first, T second) {
		this.first= first;
		this.second = second;
	}

	public T getFirst() {
		
		return this.first;
	}

	public T getSecond() {
		// TODO
		return this.second;
	}

	public void setFirst(T dt) {
		this.first = dt;
	}

	public void setSecond(T dt) {
		this.second=dt;
	}

	public void swap() {
		swapHolder = this.first;
		this.first = this.second;
		this.second=swapHolder;
		
	}

	@Override
	public String toString() {
		
		return (this.first + "," + this.second);
	}
}

